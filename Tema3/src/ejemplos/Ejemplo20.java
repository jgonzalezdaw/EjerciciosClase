package ejemplos;

/**
 * Rellenar un array bidimensional escribiendo los valores uno a uno.
 * 
 * @author csogorb
 *
 */
public class Ejemplo20 {

	public static void main(String[] args) {

		// Tabla de 4 filas por 3 columnas
		double[][] medidas = new double[4][3];

		medidas[0][0] = 8.5;
		medidas[0][1] = 1020;
		medidas[0][2] = 40;

		medidas[1][0] = 14.2;
		medidas[1][1] = 1021;
		medidas[1][2] = 35;

		medidas[2][0] = 11;
		medidas[2][1] = 1022;
		medidas[2][2] = 45;

		medidas[3][0] = 7;
		medidas[3][1] = 1023;
		medidas[3][2] = 50;

	}

}
